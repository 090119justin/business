<br>INTRODUCTION<br>

 Overview of the Current State of Technology<br>
Establishment of business taxes, permit fees and charges when applying for business is very difficult to automate considering the different practices of LGUs in imposing taxes and fees. What is applicable to one LGU may not apply to the other. Most systems resort to reprogramming and customization for those areas not handled in their program design. This is not cost-efficient and most definitely very hard to maintain. What makes it difficult is that tax imposition is not straight forward that one can simply lookup tax tables or use formulas. It is highly unstructured and dependent on information provided by the business applicant. Some proposals were made to standardize fees so that LGUs will adapt to the system, not the other way around.<br>
Renewing government permits are a necessary part of running and keeping a business. It’s a way to establish the legitimacy of your enterprise. Chris Ramasola, proprietor and chief creative director of Ramasola Visuals, says, “Clients want to make sure they are working with reliable and responsible business owners. As a business owner, you don’t want to lose potential projects just because you are unable to present updated, valid documents.”<br>
Fortunately for us, the business permits renewal process has been streamlined since the implementation of the Ease of Doing Business Act. As mandated by the central government, all local government units (LGUs) now provide one-stop application processing centers for new business registrations and renewals. Other features of the system that will help citizens on ease of access to information include businesses and business lines; computerized assessment of fees, taxes and charges; online printing of Tax Order of Payment, Business Permit; and endorsement module for the Office of the Building Official and other LGU concerned offices. At the e-BPLS Summit, DICT Secretary Salalima said that part of President Rodrigo Duterte's 10-point socioeconomic agenda is to increase competitiveness and the ease of doing business by moving towards automation. He also said that, "Taking action on the call of the President to reduce the requirements and processing time of all government transactions, the DICT has developed a cloud-based e-BPLS software that allows the taxpayers to file their application for new and renewal of business permits online, enabling the LGUs to process these applications electronically. This process which shortens the time and at the same providing up to date information will set a standard business process flow for the online processing of business permits as citizens can just log on to the LGU’s website and access the e-BPLS to submit their applications online and the scanned copies of documentary requirements.<br>
“LGUs are directed to process business registrations within two days for new business permit applications and one day for business permit renewals, requiring two signatories, namely the Mayor (or designated representative) and the treasurer as recommending approval<br>
Business permits issued by government  that allow individuals or companies to conduct business within the government's geographical jurisdiction. It is the authorization to start a business issued by the local government. A single jurisdiction often requires multiple licenses that are issued by multiple government departments. Business permits vary between countries, states, and local municipalities. There are often many licenses, registrations and certifications required to conduct a business in a single location.<br>
Typically, a company's business activity and physical location (address) determines which licenses are required to operate lawfully. Other determining factors may include the number of employees and the business, such as sole proprietor or corporation.  <br>
  According to the latest Doing Business Survey, which used Quezon City as the sample city for the Philippines, the city’s business permit issuance system had 15 procedures that took 35 days to complete from application filing to permit issuance. “The turf mentality should be eliminated and agencies should coordinate to improve (business licensing) processes,” he said in a statement, adding that this kind of attitude greatly contributed to the slowness and inefficiency of the process of issuing permits and licenses. Apart from introducing changes to the system, Bautista said a dedicated inspection team would be created to facilitate the processing of various permits.<br><br>

According to Do ing Business Survey author Sylvia Solf, local governments and regulatory offices should work together to streamline their procedures, especially as more and more businesses are acquiring business permits.   <br>
Co-author Jean Marie Lobet added that unnecessary steps in the system should be removed and the whole system be made less complex. The government should also work on reviewing and reconciling conflicting local and national laws on business regulations.<br><br>

Statement of the Problem<br>
This study aimed to determine the Online Business Permits Issuance System of Roxas City Capiz, In City Hall. <br>
 The following problems were identified in the current system.<br>
1.   It is hard to get a Business permits<br>
2.  Delayed transaction <br>
3.  Forget to transact their appointment because they are busy.<br>
4.  Wasted time due to queue.<br><br>

Objective of the Study<br>
General Objective of the Study<br>
This System raises the improvement and to assess the level of perception of the Government and the LGU’s and the User’s awareness of the Online Business Permits Issuance System.<br>  
Specific Objective<br>
1.  To provide easier process when getting business permits.<br>
2.  To provide fast transaction through online<br>
3.  To provide monitoring their accounts.<br>
4.  To provide easy process without queue.<br>

